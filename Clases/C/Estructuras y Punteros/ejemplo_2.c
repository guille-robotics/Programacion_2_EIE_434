// Creacion de un estructura llamada myStructure, y acceso a sus miembros y creacion de diferentes variables de tipo struct
#include <stdio.h>
struct myStructure {
  int myNum;
  char myLetter;
};

// Create different struct variables
struct myStructure s1;
struct myStructure s2;

// Assign values to different struct variables
s1.myNum = 13;
s1.myLetter = 'B';

s2.myNum = 20;
s2.myLetter = 'C';